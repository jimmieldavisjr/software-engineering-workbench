# Addition 
friends = 10
friends += 1
print(friends)

# Subtraction
coworkers = 10
coworkers -= 4
print(coworkers)

# Multiplcation
neighbors = 15
neighbors *= 3
print(neighbors)

# Division
# returns floats
teachers = 10
teachers /= 3
print(teachers)

# Integer Division
# return integer rounded down to whole integer
animals = 14
animals = animals // 3
print(animals)

# Modulos
apples = 20
remaining_apples = apples % 9;
print(remaining_apples)
